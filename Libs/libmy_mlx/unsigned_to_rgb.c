#include "my_mlx.h"

t_rgb		unsigned_to_rgb(unsigned int color)
{
	t_rgb r;

	r.b = ((color & 0x00FF0000) >> 24);
	r.g = ((color & 0x00FF00) >> 16);
	r.r = (color & 0x00FF);
	return (r);
}
